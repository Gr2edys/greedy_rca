# =============================================================
def parse_navi(navi_path):
    """解析 navi.xml，返回 {命令名: {cmd, category, interface, url}}"""
    with open(navi_path, 'r', encoding='utf-8') as f:
        content = f.read()

    lines = content.split('\n')
    stack = []
    results = {}

    for line in lines:
        topic_match = re.match(r'^(\s*)<topic\s+txt="([^"]+)"', line)
        if not topic_match:
            continue

        indent = len(topic_match.group(1))
        name = topic_match.group(2)
        is_linked = '/LinkedDoc/' in line
        is_doc = '/Document/' in line

        while stack and stack[-1][0] >= indent:
            stack.pop()
        stack.append((indent, name, is_linked, is_doc))

        if is_doc:
            # Extract command name
            m = re.search(r'[（(]([A-Z][A-Z\s]{2,})[）)]', name)
            cmd = m.group(1).strip() if m else name

            # Find parent LinkedDoc
            parent_cat = None
            for s in reversed(stack[:-1]):
                if s[2]:
                    parent_cat = s[1]
                    break

            url_match = re.search(r'url="([^"]+)"', line)
            url = url_match.group(1) if url_match else ''

            interface = classify_category(parent_cat or '')

            results[cmd] = {
                'cmd': cmd,
                'category': parent_cat or '',
                'interface': interface,
                'url': url
            }

    return results


def main(resources_dir, output_path):
    start = time.time()
    navi_path = os.path.join(resources_dir, 'navi.xml')

    print("解析 navi.xml ...")
    results = parse_navi(navi_path)
    print(f"  提取到 {len(results)} 个 MML 命令")

    # 统计
    from collections import Counter
    intf_count = Counter(r['interface'] for r in results.values())
    unclassified = intf_count.get('未分类', 0)

    print(f"\n{'='*60}")
    print(f"接口分类统计")
    print(f"{'='*60}")
    for intf, count in intf_count.most_common():
        pct = count / len(results) * 100
        print(f"  {intf:15s}: {count:5d} ({pct:.1f}%)")

    # 构建输出
    cmd_list = sorted(results.keys())

    output = {
        'source': 'UNC 20.13.2',
        'total_commands': len(results),
        'classified_count': len(results) - unclassified,
        'unclassified_count': unclassified,
        'interface_summary': dict(intf_count.most_common()),
        'commands': {cmd: results[cmd] for cmd in cmd_list},
    }

    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    elapsed = time.time() - start
    print(f"\n处理完成 ({elapsed:.1f}s)")
    print(f"JSON: {output_path}")


