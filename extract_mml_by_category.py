def classify_category(category_name):
    """根据命令组名返回接口分类，未匹配返回 '未分类'"""
    cat_lower = category_name.lower()
    for keyword, interface in CATEGORY_KEYWORDS:
        if keyword in cat_lower:
            return interface
    return '未分类'


